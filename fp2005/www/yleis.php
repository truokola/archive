<h3><a name="wendin">
Göran Wendin
</a></h3>
<blockquote>
<p><b>
Recent progress in hardware for scalable quantum computers
</b></p>
<p><i>pe 18.3. klo 11.00&#8211;11.45, sali 1</i></p>
<p>Research on practical devices for quantum information processing is right
now going through a very interesting stage, in Europe and worldwide.
With ion traps one has recently been able to perform quantum operations with
up to four qubits, demonstrating multi-qubit entanglement, "tests" of
Bell inequalities, and teleportation.  A major issue concerns scalability:
can these systems be scaled to many qubits in order to outperform
ordinary computers on real tasks and hard problems?  
</p>
<p>On the other hand, solid-state mesoscopic quantum electronics is
making great progress: superconducting quantum circuits with
Josephson junctions have demonstrated highly accurate coherent
operation and readout of single qubits during microseconds,
two-qubit gates have been demonstrated, and systems with three
to five qubits will soon be tested. Moreover, experiments on
electron dynamics and spin-dependent transport in mesoscopic quantum
dots are showing very promising results for qubit implementations.
</p>
<p>Therefore, the next five years promise to be decisive for our 
understanding of how easy, or difficult, it will be to fight 
decoherence in small artificial quantum systems. It is
rather meaningless to try to predict how many qubits one
will be working with in 50 years, but one will soon know
whether ten-qubit systems, in ion/atom traps or solids,
are realistic for the year 2010. This talk will present some of
the evidence for why this may be a realistic proposition.
</p>
</blockquote>


