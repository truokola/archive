Materials under pressure: new findings and phenomena
</b></p>
<p><i>pe 18.3. klo 9.45&#8211;10.30, sali 1</i></p>
<p>New static high-pressure techniques provide the ability to tune
interatomic and intermolecular interactions in condensed matter over a
broad range of conditions, from near zero pressure to &gt;300 GPa and
variable temperatures from cryogenic to thousands of degrees. Coupled with
the increasing array of measurements that can be performed under extreme
P-T conditions, including both new synchrotron and neutron techniques,
these investigations are resulting in new findings in structure, bonding,
and dynamics in solids, liquids, and fluids, as well as allowing the
creation and recovery of new materials. Examples include novel transitions
in atomic, diatomic, and polyatomic systems; structure and bonding in
molecular mixtures; pressure-induced metallization and superconductivity,
and unexpected discoveries in soft matter.
</p>
