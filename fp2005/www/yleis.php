<p><b>
Mesoscopic Josephson junctions and devices at millikelvin temperatures
</b></p>
<p><i>pe 18.3. klo 11.45&#8211;12.30, sali 1</i></p>
<p>First I am going to briefly introduce the activities at the Low Temperature
Laboratory of TKK on nanoelectronics in general. Then I move on to the
main part of my talk about phenomena in superconducting mesoscopic Josephson
junctions and SQUIDs (Superconducting Quantum Interference Devices) and
recently invented devices based on these. Small size brings new effects
into play as compared to devices consisting of traditional superconducting
tunnel junctions: I focus on discussing single Cooper pair tunnelling
effects and dissipative quantum mechanics in small junctions at low
temperatures. The devices that are based on mesoscopic Josephson junctions
at low temperatures include current pumps ultimately for the metrological
quantum triangle of electrical quantities, threshold detectors for
electrical current and its noise, and various forms of ultrasensitive
amplifiers and detectors based on single electron (single Cooper pair)
transistor effects. Most notably mesoscopic Josephson junctions are
presently intensively investigated as promising candidates for implementing
quantum bit circuits.
</p>
