<h3><a name="kragh">
Helge Kragh
</a></h3>
<blockquote>
<p><b>
The rise of physical cosmology in the 20th century
</b></p>
<p><i>to 17.3. klo 19.00&#8211;20.00, Mellin-sali, TKK</i></p>
<p>Among the many remarkable developments in 20th-century physics, the
transformation of cosmology into a physical science is perhaps the most
remarkable. In the early years of the 20th century, cosmology scarcely existed
as a science, and even with Einstein's pathbreaking theory of 1917 it remained
a field of mathematics rather than physics. The change occurred in the 1930s,
inspired by attempts to understand the cosmochemical distribution of isotopes
and the energy production of the stars. It was progress in nuclear physics that
paved the way for the first physical models of the early universe, such as
formulated in 1948 by George Gamow and his associates. Since then, the
development has accelerated and early-universe cosmology has become ever closer
connected with nuclear and particle physics. The lecture will present an
overview of the development, mainly as it occurred between the 1920s and the
emergence of the hot big bang paradigm in the 1960s.
</p>
</blockquote>


<h3><a name="grunberg">
Peter Grünberg
</a></h3>
<blockquote>
<p><b>
Spintronics in layered magnetic structures
</b></p>
<p><i>pe 18.3. klo 9.00&#8211;9.45, sali 1</i></p>
<p>Due to the presence of surfaces and interfaces in layered magnetic
structures there is a pronounced influence of the electron spin on other
electronic properties. In particular spindependent reflectivity is the
source of interlayer exchange coupling, spindependent bulk- and interface
scattering lead to Giant Magnetoresistance (GMR), spindependent tunneling
to Tunneling Magnetoresistance (TMR)  and spindependent transmission to
spin accumulation which can be used for current induced magnetic
switching. Applications are in the area of magnetic field sensors and
magnetic data storage. The current status of the field will be discussed.
</p>
</blockquote>


