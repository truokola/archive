What is inside a black hole?
</b></p>
<p><i>la 19.3. klo 12.15&#8211;13.00, sali 1</i></p>
<p>As argued many years ago by Hawking, black holes appear to
violate quantum mechanics by destroying
the reversibility of physical processes. Recent results in string
theory  however suggest a radically different picture of the black
hole interior;
  instead of being just `empty space with a central singularity', it
is now filled with a tangle of stringy matter. This picture
would resolve the `information paradox' raised by Hawking, and show
that quantum gravity effects are not confined to the planck length
but instead stretch over distances that increase with the mass of the
object being studied.</p>
