<h3><a name="khokhlov">
Alexei Khokhlov
</a></h3>
<blockquote>
<p><b>
Towards understanding and mimicking biopolymer functions: protein-like polymers
</b></p>
<p><i>pe 18.3. klo 12.30&#8211;13.15, sali 1</i></p>
<p>Recent years are characterized by a significant shift of the main
focus of polymer science from construction to functional polymers.
The requested functions of polymer systems are becoming more and more
sophisticated, giving rise to the term "smart polymers". From this
viewpoint the champions of "smartness" are biopolymers operating in
living systems (DNA, RNA, proteins), since the level of
sophistication of their functions is uncomparably higher than for any
synthetic polymers. Therefore, it is natural to adopt the so-called
biomimetic approach, i.e. to try to understand the principles of
functioning of biopolymers in living systems and to implement some of
the relevant ideas to synthetic polymers.
</p>
<p>This talk will give a review of some of the recent advances in
biomimetic polymers. In particular, I will focus on the polymers
mimicking globular proteins-enzymes (protein-like copolymers). First
problem was to realise such sequences in synthetic copolymers which
enable globule formation without precipitation. This problem was
solved due to the discovery of one-step process of "copolymerization
with simultaneous globule formation". At the next stage we tried to
arrange the conditions for enhanced catalytic function of obtained
protein-like globules. To this end we used amphiphilic monomer units
containing catalytically active groups. Due to the surface activity
of these units they are automatically inserted to globular interface.
In this case such interfaces act like surface nanoreactors, which are
especially effective for surface-active substrates. Finally, the
attempts to build-up a catalytically active center within surface
nanoreactors will be described.
</p>

</blockquote>


<h3><a name="kauranen">
Martti Kauranen
</a></h3>
<blockquote>
<p><b>
Symmetries in nonlinear optics
</b></p>
<p><i>la 19.3. klo 11.30&#8211;12.15, sali 1</i></p>
<p>Nonlinear optical effects can occur when the optical properties of
materials depend on the presence of light. Second-order nonlinear effects,
such as second-harmonic generation, are electric-dipole-forbidden in
centrosymmetric materials. They are therefore sensitive probes of
surfaces and thin molecular films deposited on substrates, where the
symmetry is necessarily broken. In addition, the details of the nonlinear
response can be correlated with the more detailed structural properties of
such samples. We review several second-order nonlinear optical effects that
arise from the structural symmetry of thin films and surfaces.
</p><p>
Chiral molecules occur in two enantiomers that are mirror images of each
other. Such molecules interact differently with left- and right-hand
circularly-polarized light. We show that the efficiency of second-harmonic
generation from chiral thin films with in-plane isotropy is also different
for the two circular polarizations. However, such circular-difference effects
can occur also for achiral thin films with in-plane anisotropy when the
alignment of the sample breaks the reflection symmetry of the experimental
setup. The symmetry of the setup can also be broken with the polarization
of light.
</p><p>
Certain organic molecules form thin films that are both chiral and anisotropic.
We demonstrate how the contributions of these structural features to
second-harmonic generation can be separated through detailed investigations
of the nonlinear response. We also demonstrate that the nonlinear response
is extremely sensitive to chiral symmetry breaking arising from small-scale
structural defects of metal nanoparticles.
</p><p>
The symmetry properties of magnetic-dipole and electric-quadrupole
interactions are different from those of electric-dipole interactions.
Such higher-order interactions allow second-order nonlinear effects also
in centrosymmetric bulk materials. Separation between the bulk and surface
contributions to the nonlinear response is essential when second-order
processes are used as surface probes. We present a technique, which is
based on fundamental symmetry properties, to achieve an unambiguous and
quantitative separation of the surface and bulk contributions to the
nonlinear response.
</p>
</blockquote>


